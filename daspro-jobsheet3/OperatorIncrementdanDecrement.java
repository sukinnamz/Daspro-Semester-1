public class OperatorIncrementdanDecrement {
    public static void main(String[] args) {
        int i = 17;

        System.out.println("i = " + i);
        System.out.println("++i = " + ++i);
        System.out.println("i++ = " + i++);
        System.out.println("--i = " + --i);
        System.out.println("i-- = " + i--);
        System.out.println("i = " + i);

    }
}
